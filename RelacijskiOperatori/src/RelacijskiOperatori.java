
public class RelacijskiOperatori {
	public static void main(String[] args)
	{
		int x = 5, y = 4, z = 5;
		
		// operator vece >
		System.out.println((y>x));
		System.out.println((x>y));
		System.out.println("");
		
		// operator vece ili jednako >=
		System.out.println((y>=x));
		System.out.println((x>=y));
		System.out.println("");
		
		// operator manje <
		System.out.println((y<x));
		System.out.println((x<y));
		System.out.println("");
		
		// operator manje ili jednako <=
		System.out.println((y<=x));
		System.out.println((x<=y));
		System.out.println("");
		
		// operator ==
		System.out.println((z==x));
		System.out.println((x==y));
		System.out.println("");
		
		// nije jednako !=
		System.out.println((z!=x));
		System.out.println((x!=y));
		System.out.println("");
	}
}
