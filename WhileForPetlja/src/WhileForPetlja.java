
public class WhileForPetlja {
	public static void main(String[] args)
	{
		int a = 0; // a je 10
		while(a<10) // kad je a 0 manje od 10
		{
			System.out.print(a);  // stampamo
			a++;            // dodajemo za 1 sve do 10
		}
		System.out.print(" ");
		int i;
		for(i=0; i<10; i++)
		{
			System.out.print(i+"");
		}
		}
	}

